    private void datTruocBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_datTruocBtnActionPerformed
        if (ds.size() == 0){
            JOptionPane.showMessageDialog(this, "Danh sách tài liệu đặt trước rỗng");
            return;
        }
        DatTruoc datTruoc = new DatTruoc();
        DocGia docGia = new DocGia();
        docGia.setSoHieu(-1);
        datTruoc.setDocGia(docGia);
        datTruoc.setDanhSachDongDatTruoc(ds);
        datTruoc.setThoiGianDat(LocalDateTime.now());
        datTruoc.guiBanDatTruoc();
        ds = new ArrayList<>();
        JOptionPane.showMessageDialog(this, "Gửi bản đặt trước thành công");
        this.setVisible(false);
    }//GEN-LAST:event_datTruocBtnActionPerformed

