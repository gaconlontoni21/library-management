    private void setup() {
        final Color BLUE_COLOR = new Color(51, 102, 255, 255);
        final Color WHITE_COLOR = new Color(255, 255, 255, 255);

        headerRenderer = new DefaultTableCellRenderer();
        headerRenderer.setBackground(BLUE_COLOR);
        headerRenderer.setForeground(WHITE_COLOR);
        headerRenderer.setHorizontalAlignment(JLabel.CENTER);
        for (int i = 0; i < jTable1.getModel().getColumnCount(); i++) {
            jTable1.getColumnModel().getColumn(i).setHeaderRenderer(headerRenderer);
            jTable1.getColumnModel().getColumn(i).setPreferredWidth(150);
        }
        jTable1.setGridColor(BLUE_COLOR);
        jTable1.setForeground(BLUE_COLOR);
        jTable1.setShowGrid(true);
        jTable1.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        ds = new ArrayList<>();
        renderTable();
    }

    private void renderTable() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        model.setColumnIdentifiers(new Object[]{"Số hiệu", "Tên", "Số lượng"});
        for (int i = 0; i < jTable1.getModel().getColumnCount(); i++) {
            jTable1.getColumnModel().getColumn(i).setHeaderRenderer(headerRenderer);
            jTable1.getColumnModel().getColumn(i).setPreferredWidth(150);
        }
        for (DongDatTruoc tl : ds) {
            TaiLieu taiLieu = tl.getTaiLieu();
            int soHieu = taiLieu.getSoHieu();
            String ten = taiLieu.getTen();
            int soLuong = tl.getSoLuong();
            model.addRow(new Object[]{soHieu, ten, soLuong});
        }
    }
    
    public void them(TaiLieu taiLieu){
        for (int i = 0; i < ds.size(); i++){
            if (ds.get(i).getTaiLieu().getSoHieu() == taiLieu.getSoHieu()){
                ds.get(i).setSoLuong(ds.get(i).getSoLuong() + 1);
                return;
            }
        }
        ds.add(new DongDatTruoc(taiLieu, 1));
    }
    
    public void openFrame(){
        this.setVisible(true);
        renderTable();
    }

