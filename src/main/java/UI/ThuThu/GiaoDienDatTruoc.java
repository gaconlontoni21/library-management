    private void choMuonBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_choMuonBtnActionPerformed
        int row = jTable1.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng dữ liệu");
            return;
        }
        if (jTable1.getModel().getValueAt(row, 0).equals("-1")) {
            JOptionPane.showMessageDialog(this, "Chưa có số hiệu độc giả");
            return;
        }
        DatTruoc dt = ds.get(row);
        Muon muon = new Muon();
        muon.setDocGia(dt.getDocGia());
        muon.setThoiGianMuon(LocalDateTime.now());
        muon.setThoiGianTraDuKien(muon.getThoiGianMuon().plusDays(14));
        ArrayList<DongMuon> dm = new ArrayList<>();
        for (DongDatTruoc ddt : dt.getDanhSachDongDatTruoc()){
            DongMuon dmm = new DongMuon();
            dmm.setTaiLieu(ddt.getTaiLieu());
            dmm.setSoLuong(ddt.getSoLuong());
            dm.add(dmm);
        }
        muon.setDanhSachDongMuon(dm);
        muon.muon(dt);
        JOptionPane.showMessageDialog(this, "Cho mượn thành công");
        ds = DatTruoc.layDanhSach();
        renderTable();
    }//GEN-LAST:event_choMuonBtnActionPerformed

