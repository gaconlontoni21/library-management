    private void timTheoNgayBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_timTheoNgayBtnActionPerformed
        ArrayList<LocalDate> dates = ThongKe.getMinMaxDate("thoiGianDat", "DatTruoc");
        if (dates == null){
            return;
        }
        LocalDate bd = dates.get(0);
        LocalDate kt = dates.get(1);
        if (ngayBatDauDC.getDate() != null) {
            bd = ngayBatDauDC.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        }
        if (ngayKetThucDC.getDate() != null) {
            kt = ngayKetThucDC.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate();
        }
        ds = DatTruoc.timKiemTheoThoiGian(bd, kt);
        renderTable();
    }//GEN-LAST:event_timTheoNgayBtnActionPerformed

    private void datTruocBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_datTruocBtnActionPerformed
        int row = jTable1.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn bản đặt trước");
            return;
        }
        if (!jTable1.getModel().getValueAt(row, 0).equals("-1")) {
            JOptionPane.showMessageDialog(this, "Đã được đặt trước");
            return;
        }
        ArrayList<ConNguoi> dsDocGia = new DocGia().layDanhSach();
        Integer[] pos = new Integer[dsDocGia.size()];
        for (int i = 0; i < pos.length; i++) {
            pos[i] = dsDocGia.get(i).getSoHieu();
        }
        Integer soHieu = (Integer) JOptionPane.showInputDialog(
                this,
                "Chọn số hiệu độc giả",
                "",
                JOptionPane.PLAIN_MESSAGE,
                null,
                pos,
                pos[0]);
        if (soHieu != null) {
            DatTruoc dt = ds.get(row);
            DocGia dg = new DocGia();
            dg.setSoHieu(soHieu);
            dt.setDocGia(dg);
            dt.datTruoc();
            JOptionPane.showMessageDialog(this, "Đặt trước thành công");
            ds = DatTruoc.layDanhSach();
            renderTable();
        }
    }//GEN-LAST:event_datTruocBtnActionPerformed

    private void timTheoSHDocGiaBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_timTheoSHDocGiaBtnActionPerformed
        if (soHieuDocGiaTf.getText().equals("")) {
            ds = DatTruoc.layDanhSach();
            renderTable();
            return;
        }
        try {
            int soHieu = Integer.valueOf(soHieuDocGiaTf.getText());
            ds = DatTruoc.timKiemTheoSoHieuDocGia(soHieu);
        } catch (NumberFormatException e) {
            ds = new ArrayList<>();
        }
        renderTable();
    }//GEN-LAST:event_timTheoSHDocGiaBtnActionPerformed

